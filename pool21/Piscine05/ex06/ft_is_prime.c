int		ft_is_prime(int nb)
{
	if (nb <= 1)
		return (0);
	if (nb == 3 || nb == 5 || nb == 7 || nb == 2)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0 || nb % 4 == 0 || nb % 5 == 0 ||
			nb % 6 == 0 || nb % 7 == 0 || nb % 8 == 0 || nb % 9 == 0)
		return (0);
	else
		return (1);
}
int main()

{
    printf("%d\n",ft_is_prime(121));
}
